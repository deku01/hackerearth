import java.util.*;
class Ladderophilia{
    public static void main(String args[] ) throws Exception {
        Scanner s = new Scanner(System.in);
        int n = s.nextInt();
        System.out.println("*   *");
        System.out.println("*   *");
        for(int i=0;i<n;i++){
            System.out.println("*****");
            System.out.println("*   *");
            System.out.println("*   *");
        }
    }
}